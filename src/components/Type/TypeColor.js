


export function typeColor(poketype){
    let pokecolor;
    switch (poketype) {
    case 'normal':
        pokecolor = "#aaaa99";
        return pokecolor
        
    case 'fire':
        pokecolor = "#ff4422";
        return pokecolor
        
    case 'water':
        pokecolor = "#3399ff";
        return pokecolor
        
    case 'grass':
        pokecolor = "#77cc55";
        return pokecolor
        
    case 'electric':
        pokecolor = "#ffcc33";
        return pokecolor
        
    case 'ice':
        pokecolor = "#66ccff";
        return pokecolor
        
    case 'fighting':
        pokecolor = "#bb5544";
        return pokecolor
        
    case 'poison':
        pokecolor = "#aa5599";
        return pokecolor
        
    case 'ground':
        pokecolor = "#ddbb55";
        return pokecolor
        
    case 'flying':
        pokecolor = "#8899ff";
        return pokecolor
        
    case 'psychic':
        pokecolor = "#ff5599";
        return pokecolor
        
    case 'bug':
        pokecolor = "#aabb22";
        return pokecolor
        
    case 'rock':
        pokecolor = "#bbaa66";
        return pokecolor
        
    case 'ghost':
        pokecolor = "#6666bb";
        return pokecolor
        
    case 'dragon':
        pokecolor = "#7766ee";
        return pokecolor
        
    case 'dark':
        pokecolor = "#775544";
        return pokecolor
        
    case 'steel':
        pokecolor = "#aaaabb";
        return pokecolor
        
    case 'fairy':
        pokecolor = "#ee99ee";
        return pokecolor
    default:
        pokecolor = "#122fff";
        console.log(pokecolor)
        return pokecolor    
    
    } 
}

